import java.util.Iterator;

public class numero17 {
    public static void main(String[] args) {
        //iteraciones
        int i = 0;
        do{i++;
            System.out.println(i);
        }while(i<10);
        i=0;
        System.out.println();
        while(i<10){
            i++;
            System.out.println(i);
        }
        System.out.println();
        for (int j = 0; j <= 10; j++) {
            System.out.println(j);
        }

    }
}
